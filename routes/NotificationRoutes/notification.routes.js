import {
  sendNotification,
  getAllNotificationsOfAUser,
  deleteNotification,
  deleteNotificationsOfAUser,
} from "../../controllers/NoticationControllers/notification.controllers.js";
import express from "express";
import verifyAuth from "../../middlewares/verifyAuth.js";
const router = express.Router();

router.post("/send/:userId",verifyAuth, sendNotification);
router.delete("/delete-one/:notificationId",verifyAuth, deleteNotification);
router.delete("/delete-all/:userId",verifyAuth, deleteNotificationsOfAUser);
router.get("/all/:userId",verifyAuth,verifyAuth, getAllNotificationsOfAUser);

export default router;
